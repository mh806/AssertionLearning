package main.java;

public class Calculator {
  public int Add(int a, int b){
    int result = a + b;
    return result;
  }

  public int Deduct(int a, int b){
    int result = a - b;
    return result;
  }

  public int Multiple(int a, int b){
    int result = a * b;
    return result;
  }

  public int Divide(int a, int b){
    int result = a / b;
    return result;
  }

  public int getResult(){
    return this.getResult();
  }
}
